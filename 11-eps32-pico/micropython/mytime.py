def localtime():
    t = utime.time()
    t2 = t + 10957 * 24 * 3600
    for i in range(len(tzdata)):
        if tzdata[i] > t2:
            t += 3600 + 3600 * (i%2)
            break
    return utime.gmtime(t)

def longtime():
    t = localtime()
    return "{} {} {} {}, {:02d}:{:02d}:{:02d}".format(
        dagen[t[6]],
        t[2], maanden[t[1]], t[0],
        t[3], t[4], t[5])

def shorttime():
    t = localtime()
    return "{:02d}-{:02d}-{:04d} {:02d}:{:02d}:{:02d}".format(
        t[2], t[1], t[0],
        t[3], t[4], t[5])

